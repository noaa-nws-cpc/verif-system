<p>
This is an interactive tool that allows users to calculate the skill of forecasts, which is a relative measure of how the forecasts performed. Knowledge of forecast skill can help users when using forecasts for decision making.
</p>
<p>
<b>Instructions for use:</b>
<ol>
	<li>Select type of skill score output display desired ("Chart" or "Map").
	<li>Select Forecast and Verification Options.
	<li>For "Chart", select region(s) to verify ("Climate Regions","States","Climate Divisions"). For "Map", select dates(s) to verify ("Date Range","Months/Seasons & Years","Climate Phenomena").
	<li>Click the "Get Scores" button.
</ol>
</p>
