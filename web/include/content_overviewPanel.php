<p>
<b>Results display:</b>
</p>
<p>
The results are displayed below the options area and you should see a "Processing, please wait" message until processing is complete. A legend is also displayed with the results.
</p>
<p>
<b>Error panel:</b>
</p>
<p>
Below the results display is a panel that displays any warnings or errors that have occurred during processing and these messages can be reported to CPC help support.
</p>

<p>
<b>Information panel:</b>
</p>
<p>
Below the warnings and errors panel is the information panel which will also update after processing is complete and contains a brief statistics summary report, information about the results and the type of skill score selected.
</p>
